package D3数组遍历;

public class 数组遍历 {

 //   数组遍历：将数组中所有的内容取出来，取出来之后可以（打印，求和，判断．.)
 //   注意：遍历指的是取出数据的过程，不要局限的理解为，遍历就是打印！

    public static void main(String[] args) {
        int[]arr = {1,2,3,4,5,6};
        //数组名.length 表示数组的长度




        //使用循环进行遍历  ：

        for (int i = 0;i<=arr.length -1;i++){
            System.out.println(arr[i]);
        }



        //快捷方式： 数组名.fori



    }


}
