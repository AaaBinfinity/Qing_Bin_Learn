package D3循环结构;
//需求1：打印1-5
//需求2：打印5-1
public class Practice输出数字 {
    public static void main(String[] args) {

        //打印1-5
        for (int i = 1;i<=5;i++){
            System.out.println(i);
        }


        //打印5-1
        for (int j = 5;j>0;j--){
            System.out.println(j);
        }
    }

}
