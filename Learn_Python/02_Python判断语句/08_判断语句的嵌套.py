# 判断语句的嵌套

'''
                                  满足——→Do Something
             满足——→如果——→判断——→
                                  不满足——→Do Something
如果——→判断——→
                                  满足——→Do Something
             不满足——→如果——→判断——→
                                  不满足——→Do Something
许多逻辑的判断，是嵌套的，我们可以：自由组合if elif else，完成特定需求的要求
'''

# 基础语法格式
'''
if 条件1：
    满足条件1 做的事情1
    满足条件2 做的事情2
    if 条件2：
    满足条件2 做的事情1
'''
