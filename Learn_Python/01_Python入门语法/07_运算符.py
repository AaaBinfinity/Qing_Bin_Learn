# 算数（数字）运算符
print("1 + 1 = ", 1 + 1)
print("1 - 1 = ", 1 - 1)
print("1 * 1 = ", 1 * 1)
print("14 / 13 = ", 14 / 13)
print("14 // 13 = ", 14 // 13)
print("14 % 3 = ", 14 % 3)
print("14 ** 13 = ", 14 ** 13)

# 赋值运算符     “=” 将后面的值赋给前面的变量活其他函数
num = 12345 + 23
print(num)

# 复合赋值运算符       “+=”、“-=”、“*=”、“/=”、“%=”、“**-”、“//=”   将变量本身进行运算之后再给前面的变量进行赋值
num = 7
num += 1
print(num)
